                    <p>La carga de pólenes atmosféricos depende en gran medida de la pluviosidad que condiciona la germinación y crecimiento de las hierbas. Cada planta tiene su periodo de floración y polinización, en el que intervienen factores como la temperatura, la altura o la latitud. Durante el periodo de polinización las concentraciones atmosféricas aumentan con el incremento de temperatura (días secos y soleados) y disminuyen con la lluvia o frío. Las mayores concentraciones son por la mañana, cuando las plantas emiten los pólenes, y el atardecer, ya que al enfriarse el aire el polen baja al nivel del suelo. En las ciudades la concentración es menor que en las zonas rurales debido al efecto barrera de los edificios y al efecto cale, producido por el calor del cemento y el asfalto que origina corrientes ascendentes que arrastran el polen a alturas no respirables.</p>
