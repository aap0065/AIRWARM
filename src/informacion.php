            <div id="A">
                <h3> Tipos</h3>
                <div id="asmaT">
                    <p>Existen diferentes clasificaciones del asma. En cuanto a su origen se puede dividir en asma intrínseca y asma extrínseca o alérgica</p>
                    <ul>
                        <li>El origen del asma intrínseca es desconocido y se detecta con mayor frecuencia en la edad adulta. Tiene un peor pronóstico que el de carácter alérgico y tiende a cronificarse.</li>
                        <li>El asma extrínseca, por su parte, consiste en una reacción antígeno-anticuerpo que desencadena el proceso. Afecta principalmente a niños y adultos jóvenes, se caracteriza por ataques reversibles y breves de broncoespasmos con silbidos y dificultad respiratoria, y se controla con un tratamiento adecuado.</li>
                    </ul>
                </div>
                <h3>Causas</h3>
                <div id="asmaC">
                    <p>En muchos casos la enfermedad aparece como respuesta a determinados estímulos que producen alergia: polen, ácaros del polvo, partículas de la piel de gato y perro, humo, aire frío, ciertos alimentos o aditivos alimenticios. </p>
                    <p>Pólenes: Aunque la polinización se produce durante la primavera, existen variaciones según los climas y tipos de plantas. Los síntomas de la alergia al polen suelen detectarse con concentraciones superiores a 50 granos de polen por metro cúbico de aire. El pequeño tamaño del polen favorece que quede suspendido en el aire durante largo tiempo y recorra grandes distancias hasta penetrar en los conductos respiratorios.</p>
                    <p>Ácaros del polvo: Son parásitos microscópicos que viven en el polvo de las casas y se alimentan de escamas dérmicas y otros residuos. Necesitan unas condiciones precisas para desarrollarse: 25º C de temperatura y 85 por ciento de humedad.</p>
                    <p>Alimentos: Los episodios de asma relacionados con alimentos son frecuentes durante la infancia y van acompañados de otros síntomas como urticaria y vómitos, por lo que tienden a confundirse con intoxicaciones alimentarias. Los productos que mayores reacciones provocan son la leche, los huevos y el pescado. En la edad adulta este tipo de asma es menos frecuente.</p>
                    <p>Hongos: Algunos hongos producen alergenos que se depositan en sus esporas, y la liberación de éstas depende de la humedad, la temperatura y la existencia de materia orgánica en su entorno.</p>
